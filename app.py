    "        st.error(\"Vui lòng nhập từ khóa!\")\n",
    "        st.stop()\n",
    "\n",
    "    keywords = [kw.strip() for kw in keywords_str.split(',') if kw.strip()]\n",
    "    \n",
    "    with st.spinner(\"Đang crawl (rất nhanh vì không dùng Selenium)...\"):\n",
    "        all_data = []\n",
    "        for kw in keywords:\n",
    "            all_data.extend(crawl_vnexpress(kw))\n",
    "            all_data.extend(crawl_cafef(kw))\n",
    "            time.sleep(1)  # tránh bị block\n",
    "\n",
    "    df = pd.DataFrame(all_data)\n",
    "    df_clean = df.drop_duplicates(subset=['Tiêu đề'])\n",
    "    df_new = df_clean.copy()\n",
    "\n",
    "    # === LỌC NGÀY (giữ nguyên logic cũ) ===\n",
    "    st.write(f\"🔍 Đang lọc bài SAU ngày: {target_date}\")\n",
    "    keep_indices = []\n",
    "    article_dates = []\n",
    "    progress_bar = st.progress(0)\n",
    "\n",
    "    for i, (idx, row) in enumerate(df_new.iterrows()):\n",
    "        url = str(row.get('Link', '')).strip()\n",
    "        if not url: continue\n",
    "        \n",
    "        try:\n",
    "            resp = requests.get(url, headers=headers, timeout=10)\n",
    "            soup = BeautifulSoup(resp.text, 'html.parser')\n",
    "            \n",
    "            date_text = None\n",
    "            meta = soup.find('meta', attrs={'property': 'article:published_time'}) or soup.find('meta', attrs={'name': ['article:published_time', 'pubdate', 'publishdate']})\n",
    "            if meta and meta.get('content'):\n",
    "                date_text = meta['content']\n",
    "            \n",
    "            if not date_text:\n",
    "                time_tag = soup.find('time')\n",
    "                if time_tag and time_tag.get('datetime'):\n",
    "                    date_text = time_tag['datetime']\n",
    "            \n",
    "            if not date_text:\n",
    "                if \"vnexpress.net\" in url:\n",
    "                    tag = soup.find('span', class_='date') or soup.find('time')\n",
    "                else:\n",
    "                    tag = soup.find('span', class_='pdate') or soup.find('span', class_='time')\n",
    "                if tag:\n",
    "                    date_text = tag.get_text(strip=True)\n",
    "            \n",
    "            if not date_text:\n",
    "                continue\n",
    "\n",
    "            parsed_date = None\n",
    "            iso_match = re.search(r'(\\d{4}-\\d{2}-\\d{2})', date_text)\n",
    "            if iso_match:\n",
    "                try:\n",
    "                    parsed_date = datetime.strptime(iso_match.group(1), \"%Y-%m-%d\").date()\n",
    "                except:\n",
    "                    pass\n",
    "            \n",
    "            if not parsed_date:\n",
    "                dmy_match = re.search(r'(\\d{1,2})[./-](\\d{1,2})[./-](\\d{4})', date_text)\n",
    "                if dmy_match:\n",
    "                    d, m, y = dmy_match.groups()\n",
    "                    try:\n",
    "                        parsed_date = datetime.strptime(f\"{d}/{m}/{y}\", \"%d/%m/%Y\").date()\n",
    "                    except:\n",
    "                        pass\n",
    "            \n",
    "            if not parsed_date:\n",
    "                parsed = dateparser.parse(date_text, languages=['vi'], settings={'DATE_ORDER': 'DMY'})\n",
    "                if parsed:\n",
    "                    parsed_date = parsed.date()\n",
    "            \n",
    "            if parsed_date and parsed_date > target_date:\n",
    "                keep_indices.append(idx)\n",
    "                article_dates.append(parsed_date.strftime(\"%d/%m/%Y\"))\n",
    "                \n",
    "        except:\n",
    "            pass\n",
    "        \n",
    "        progress_bar.progress((i+1)/len(df_new))\n",
