    "def crawl_vnexpress(keyword):\n",
    "    data = []\n",
    "    base_url = f\"https://timkiem.vnexpress.net/?search_q={quote(keyword)}\"\n",
    "    st.write(f\"Đang crawl VnExpress: **{keyword}**\")\n",
    "    \n",
    "    for page in range(1, 3):\n",
    "        url = base_url if page == 1 else f\"{base_url}&page={page}\"\n",
    "        resp = requests.get(url, headers=headers, timeout=10)\n",
    "        soup = BeautifulSoup(resp.text, 'html.parser')\n",
    "        articles = soup.find_all('h3', class_='title-news')\n",
    "        st.write(f\"  Trang {page}: Tìm thấy {len(articles)} bài\")\n",
    "        \n",
    "        for article in articles:\n",
    "            try:\n",
    "                a_tag = article.find('a')\n",
    "                title = a_tag.text.strip()\n",
    "                if keyword.lower() not in title.lower(): continue\n",
    "                link = a_tag.get('href')\n",
    "                if link and \"vnexpress.net\" in link:\n",
    "                    data.append({'Keyword': keyword, 'Tiêu đề': title, 'Link': link})\n",
    "            except:\n",
    "                continue\n",
    "    return data\n",
    "\n",
    "def crawl_cafef(keyword):\n",
    "    data = []\n",
    "    base_url = f\"https://cafef.vn/tim-kiem.chn?keywords={quote(keyword)}\"\n",
    "    st.write(f\"Đang crawl CafeF: **{keyword}**\")\n",
    "    \n",
    "    for page in range(1, 3):\n",
    "        url = base_url if page == 1 else f\"https://cafef.vn/tim-kiem/trang-{page}.chn?keywords={quote(keyword)}\"\n",
    "        resp = requests.get(url, headers=headers, timeout=10)\n",
    "        soup = BeautifulSoup(resp.text, 'html.parser')\n",
    "        articles = soup.find_all('a', href=lambda h: h and '.chn' in h and 'tim-kiem' not in h)\n",
    "        st.write(f\"  Trang {page}: Tìm thấy {len(articles)} bài\")\n",
    "        \n",
    "        for article in articles:\n",
    "            title = article.text.strip()\n",
    "            if keyword.lower() not in title.lower(): continue\n",
    "            link = article.get(\"href\")\n",
    "            data.append({'Keyword': keyword, 'Tiêu đề': title, 'Link': \"https://cafef.vn\" + link})\n",
    "    return data\n",
    "\n",
    "if st.button(\"🚀 BẮT ĐẦU CRAWL & LỌC\", type=\"primary\", use_container_width=True):\n",
